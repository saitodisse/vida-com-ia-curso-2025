# O que são Perceptrons?
# Perceptrons são modelos de redes neurais artificiais que são capazes de aprender a partir de dados de entrada e saída.
# Eles são compostos por uma camada de entrada, uma camada oculta e uma camada de saída.
# A camada de entrada é onde os dados de entrada são inseridos.
# A camada oculta é onde os dados são processados e transformados.
# A camada de saída é onde os dados de saída são produzidos.

# Sua formula é:
# y = f(w1*x1 + w2*x2 + ... + wn*xn + b)
# onde:
# y é a saída
# f é a função de ativação
# w1, w2, ..., wn são os pesos
# x1, x2, ..., xn são os dados de entrada
# b é o bias

# A função de ativação mais comum é a função sigmoide:
# f(x) = 1 / (1 + e^(-x))

# A função sigmoide é uma função que transforma qualquer valor em um valor entre 0 e 1.
# Ela é uma função contínua e diferenciável, o que é importante para o treinamento das redes neurais.

